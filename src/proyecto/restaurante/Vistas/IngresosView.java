    private void borrarFila(){
        int f=jtListaDetalles.getRowCount()-1;
        for(;f>=0;f--){
            dtm.removeRow(f);
        }
    }
    private void armarCabecera(){
        dtm.addColumn("Mesero/s");
        dtm.addColumn("Apellido");
        dtm.addColumn("Mesa/s");
        dtm.addColumn("Importe");
        jtListaDetalles.setModel(dtm);
    }
